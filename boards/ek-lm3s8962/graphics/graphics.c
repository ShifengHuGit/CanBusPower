//*****************************************************************************
//
// Display scrolling text plus graphics on the OLED display.
//
//*****************************************************************************
int
main(void)
{
    unsigned long ulRow, ulCol;
    volatile int iDelay;
    static unsigned char pucBuf[64];
    static char pucHello[] =
    {
        "                      "
        "See How Far Your Dollar Can Go!"
        "                      "
    };

    //
    // Set the clocking to run directly from the crystal.
    //
    SysCtlClockSet(SYSCTL_SYSDIV_1 | SYSCTL_USE_OSC | SYSCTL_OSC_MAIN |
                   SYSCTL_XTAL_8MHZ);

    //
    // Initialize the OLED display.
    //
    RIT128x96x4Init(1000000);

    //
    // Display the BMP Image.
    // The image is a 55 row by 128 column 4-bit gray scale image
    // in BMP format.
    //
    for(ulRow = 0; ulRow < 55; ulRow++)
    {
        for(ulCol = 0; ulCol < 64; ulCol++)
        {
            //
            // Invert the the pixel values.
            //
            pucBuf[ulCol] = ~g_pucLogo[ulRow][ulCol];
        }

        //
        // Display in reverse row order.
        //
        RIT128x96x4ImageDraw(pucBuf, 0, (80 - ulRow), 128, 1);
    }

    //
    // Simple scrolling text display
    //
    ulCol = 0;
    while(1)
    {
        //
        // Display the text.
        //
        RIT128x96x4StringDraw(&pucHello[ulCol++], 0, 0, 11);

        //
        // Delay for a bit.
        //
        for(iDelay = 0; iDelay < 100000; iDelay++)
        {
        }

        //
        // Wrap the index back to the beginning of the string.
        //
        if(ulCol > 53)
        {
            ulCol = 0;
        }
    }
}
